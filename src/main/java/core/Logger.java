package core;

public class Logger {
    private Logger log;

    public void info(String message) {
        log.info(message);
    }

    public void debug(String message) {
        log.info(message);
    }

    public void trace(String message) {
        log.info(message);
    }

    public void warn(String message) {
        log.info(message);
    }

    public void fatal(String message) {
        log.info(message);
    }

}
